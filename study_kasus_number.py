# Meminta pengguna untuk memasukkan dua angka
angka1 = float(input("Masukkan angka pertama: "))
angka2 = float(input("Masukkan angka kedua: "))

# Melakukan operasi matematika pada angka-angka yang dimasukkan
penambahan = angka1 + angka2
pengurangan = angka1 - angka2
perkalian = angka1 * angka2
pembagian = angka1 / angka2

# Menampilkan hasil operasi matematika
print("Hasil penambahan:", penambahan)
print("Hasil pengurangan:", pengurangan)
print("Hasil perkalian:", perkalian)
print("Hasil pembagian:", pembagian)
